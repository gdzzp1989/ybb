package ybb;

public class Major {

    private String mjInfo;//专业介绍
    private String learningContent;//学习内容
    private String prospects;//就业前景
    private String workDirection;//就业方向
    private String workPost;//就业岗位

    public String getMjInfo() {
        return mjInfo;
    }

    public void setMjInfo(String mjInfo) {
        this.mjInfo = mjInfo;
    }

    public String getLearningContent() {
        return learningContent;
    }

    public void setLearningContent(String learningContent) {
        this.learningContent = learningContent;
    }

    public String getProspects() {
        return prospects;
    }

    public void setProspects(String prospects) {
        this.prospects = prospects;
    }

    public String getWorkDirection() {
        return workDirection;
    }

    public void setWorkDirection(String workDirection) {
        this.workDirection = workDirection;
    }

    public String getWorkPost() {
        return workPost;
    }

    public void setWorkPost(String workPost) {
        this.workPost = workPost;
    }
}
